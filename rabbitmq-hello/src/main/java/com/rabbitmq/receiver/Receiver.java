/*
* 类描述：消息接收者
* @auther linzf
* @create 2018/1/4 0004 
*/
@Component
@RabbitListener(queues = "hello")
public class Receiver {

    @RabbitHandler
    public void process(String hello) {
        System.out.println("Receiver : " + hello);
    }

    }
